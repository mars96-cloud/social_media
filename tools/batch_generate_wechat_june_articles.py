def normalize_intro(intro: str | list[str]) -> list[str]:
    if isinstance(intro, str):
        return [intro]
    return intro


def build_decision_card(spec: dict) -> str:
    return "\n".join(
        [
            "# 内容决策卡",
            "1. 目标人群",
            "",
            "内容创作者 / 个体经营者 / 想把 AI 真正用于工作流和轻变现的人。",
            "",
            "2. 内容支柱",
            "",
            spec["pillar"],
            "",
            "3. 当前目标",
            "",
            spec["current_goal"],
            "",
            "4. 选题判断",
            "",
            spec["topic_judgement"],
            "",
            "5. 主 CTA",
            "",
            f"公众号后台回复 `{spec['cta_keyword']}`，领取《{spec['cta_resource']}》。",
            "",
            "# 标题",
            "",
            spec["title"],
            "",
            "# 导语",
            "",
            *normalize_intro(spec["intro"]),
            "",
            "# 正文结构",
            "",
        ]
    )


def build_publish_md(spec: dict) -> str:
    lines = [build_decision_card(spec)]
    for section in spec["sections"]:
        lines.append(f"## {section['heading']}")
        lines.append("")
        lines.append(f"### {section['cue']}")
        lines.append("")
        for paragraph in section["paragraphs"]:
            lines.append(paragraph)
            lines.append("")
        lines.append(f"【配图：{section['image']['file']}】")
        lines.append("")
    lines.append("## 结尾承接")
    lines.append("")
    for paragraph in spec["closing"]:
        lines.append(paragraph)
        lines.append("")
    return "\n".join(lines).strip() + "\n"


def build_draft_body(spec: dict) -> str:
    lines = [spec["title"], ""]
    for paragraph in normalize_intro(spec["intro"]):
        lines.append(paragraph)
        lines.append("")
    lines.append("【配图：01_公众号封面.png】")
    lines.append("")

    for section in spec["sections"]:
        lines.append(section["heading"])
        lines.append("")
        lines.append(section["cue"])
        lines.append("")
        for paragraph in section["paragraphs"]:
            lines.append(paragraph)
            lines.append("")
        lines.append(f"【配图：{section['image']['file']}】")
        lines.append("")

    for paragraph in spec["closing"]:
        lines.append(paragraph)
        lines.append("")

    return "\n".join(lines).strip() + "\n"


def build_image_plan(spec: dict) -> str:
    lines = [
        "# 公众号配图方案",
        "",
        "## 文章信息",
        "",
        f"- 文章：{spec['title']}",
        "- 目标：提升完读率，减少纯文字压迫感，让关键判断都有配图承接",
        "- 风格：米白底、深绿字、橙色强调，专业、清晰、有信息图感",
        "",
        "## 配图总原则",
        "",
        "1. 文中图不是装饰图，而是理解图。",
        "2. 正文每一节至少有 1 张图承接该节核心判断。",
        "3. 图中只讲 1 个重点，不把一张图做成信息堆砌海报。",
        "4. 首图负责点题，正文图负责解释、对比、步骤和清单。",
        "",
        "## 配图清单",
        "",
        "### 1. 头图 / 封面",
        "- 文件：`01_公众号封面.png`",
        "- 用途：文章开头首图",
        "- 作用：点题，建立统一视觉基调",
        "",
    ]
    for idx, section in enumerate(spec["sections"], start=2):
        lines.extend(
            [
                f"### {idx}. {section['image']['title']}",
                f"- 文件：`{section['image']['file']}`",
                f"- 插入位置：{section['heading']} 后",
                f"- 作用：{section['image']['footer']}",
                "",
            ]
        )

    lines.extend(
        [
            "## 转化承接建议",
            "",
            f"1. 本文主 CTA 固定为“回复 `{spec['cta_keyword']}` 领取《{spec['cta_resource']}》”",
            "2. 不在同一篇文章里并列加入多个主 CTA，避免分散动作",
            "3. 正文承接顺序固定为：",
            "   - 先讲问题或误区",
            "   - 再讲方法、路径或判断",
            "   - 最后承接可直接拿去用的资料入口",
            "",
        ]
    )
    return "\n".join(lines).strip() + "\n"


def render_cover(spec: dict, image_dir: Path) -> None:
    cover_base = image_dir / "wechat_cover_base.png"
    cover_title = spec["title"]
    cover_subtitle = f"回复 {spec['cta_keyword']} 领取《{spec['cta_resource']}》"
    render_wechat_cover(image_dir / "01_公众号封面.png", cover_base, cover_title, cover_subtitle)


def render_diagrams(spec: dict, image_dir: Path) -> None:
    for section in spec["sections"]:
        info = section["image"]
        render_wechat_diagram(
            image_dir / info["file"],
            info["title"],
            info["items"],
            info["footer"],
        )


def build_folder_name(spec: dict) -> str:
    return f"{spec['date']}_公众号_{spec['folder_title']}"


def write_article(spec: dict) -> Path:
    article_dir = DRAFT_DIR / build_folder_name(spec)
    image_dir = article_dir / "images"
    image_dir.mkdir(parents=True, exist_ok=True)

    (article_dir / "发布稿.md").write_text(build_publish_md(spec), encoding="utf-8")
    (article_dir / "草稿箱正文.txt").write_text(build_draft_body(spec), encoding="utf-8")
    (article_dir / "配图方案.md").write_text(build_image_plan(spec), encoding="utf-8")
    (article_dir / "metadata.json").write_text(
        json.dumps(
            {
                "title": spec["title"],
                "date": spec["date"],
                "cta_keyword": spec["cta_keyword"],
                "cta_resource": spec["cta_resource"],
                "image_files": ["01_公众号封面.png"] + [section["image"]["file"] for section in spec["sections"]],
            },
            ensure_ascii=False,
            indent=2,
        ),
        encoding="utf-8",
    )

    render_cover(spec, image_dir)
    render_diagrams(spec, image_dir)
    return article_dir


def main() -> int:
    DRAFT_DIR.mkdir(parents=True, exist_ok=True)
    created = [str(write_article(spec)) for spec in ARTICLES]
    manifest = MONTH_DIR / "wechat_batch_manifest_20260621_20260630.json"
    manifest.write_text(json.dumps(created, ensure_ascii=False, indent=2), encoding="utf-8")
    print(json.dumps({"created": created, "manifest": str(manifest)}, ensure_ascii=False, indent=2))
    return 0


